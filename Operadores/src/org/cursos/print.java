package org.cursos;
/* El programa debe mostrar «2 más 3 es igual a 5» en la pantalla. */
public class Solution {
	public static void main(String[] args) {
		int a = 3;
		int b = 2;
        
		System.out.print(b);
        System.out.print(" más ");
        System.out.print(a);        
        System.out.print(" es igual a ");
        System.out.print(a + b);
	}
}